package start;

public class Basic {

	public static void main(String[] args) { //main 입력 후 ctrl+spacebar
//클래스 명과 java파일명은 동일한 이름으로 설정해야 한다
//		main 메소드를 entry point(프로그램 진입점)으로 인식하여 파일 실행을 한다.
		System.out.println("오늘은 java 2번째 날"); //syso 입력 후 ctrl+spacebar
		System.out.println("java file을 실행하려면 ctrl+F11");
		System.out.println("println()은 줄바꿈을 하여 출력하라는 의미");
		System.out.println("ctrl+alt+↓ : 윗줄 복제");
//		한 줄 주석은 한 줄이 선택되어 있는 상태에서 ctrl+/
		System.out.println("한 줄 주석은 한 줄이 선택되어 있는 상태에서 ctrl+/");
		/*
		 * 여러줄 주석은 여러줄이 선택되어 있는 상태에서
		 * ctrl+shift+/
		 */
		System.out.println("여러줄 주석은 여러줄이 선택되어 있는 상태에서 ctrl+shift+/");
		
	}

}
