package ex05;

public class Area {
	double base;
	double height;

	public double getBase() {
		return base;
	}

	public double getHeight() {
		return height;
	}

	public void setBase(double base) {
		this.base = base;
	}

	public void setHeight(double height) {
		this.height = height;
	}

	public Area(double base, double height) {
		super();
		this.base = base;
		this.height = height;
	}

}
