package geomji_java;

public class Exam05 {

	public static void main(String[] args) {
		// 관계 연산자 : 연산식을 옳고 그름의 여부를 true or false로 반환
		System.out.println(5 < 3);
		System.out.println(5 > 3);
		System.out.println(5 <= 3);
		System.out.println(5 >= 3);
		System.out.println(5 == 3);
		System.out.println(5 == 5); // == 같다  a=b : 대입하겠다
		System.out.println(5 != 5); // != 같지 않다
		System.out.println(5 != 6);

	}

}
