package geomji_java;

public class Exam02 {

	public static void main(String[] args) {
		int n1 = 100;
		System.out.println(n1);

		n1 = n1 + 100;
		System.out.println(n1);
		
		n1 += 100;
		System.out.println(n1);

		n1 -= 100;
		System.out.println(n1);
		
		n1 *= 100;
		System.out.println(n1);
		
		n1 /= 100; // 몫을 구하는 계산
		System.out.println(n1);
		
		n1 %= 100; // 나머지를 구하는 계산
		System.out.println(n1);
		
	
	}

}
